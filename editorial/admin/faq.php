        <h3 class="hndle"><span><?php _e('How do I install Editorial theme?', 'Editorial'); ?></span></h3>
        <div class="inside">
          <div class="table table_content">
          <p>Don't worry, this is so simple it's almost trivial. You can do it even if you are not in best terms with technical things. There are two ways of doing it. After installing WordPress, just select the preferred method and follow the steps.</p>
          <h4>Using the FTP client</h4>
          <ol>
          <li>Download the ZIP archive and extract the files it contains. You need to preserve the directory structure in the archive when extracting these files.</li>
          <li>Using an FTP client to access your host web server, upload the folder containing Editorial theme in the wp-content/themes directory provided by WordPress. Your end folder structure should be wp-content/themes/Editorial.</li>
          <li>Follow the instructions below for enabling the Editorial theme.</li>
          </ol>
          <h4>Using the Administration Panel</h4>
          <p>You can download Themes directly to your blog by using the Add New Themes option in the Appearance sub-menu.</p>
          <ol>
          <li>Log in to the WordPress Administration Panel.</li>
          <li>Select the Appearance panel, then Themes.</li>
          <li>Select Install Themes</li>
          <li>Use the sub-menu or the Search or Filter options to locate Editorial theme.</li>
          <li>Click on the Preview link to preview the Editorial theme or the Install link to upload Editorial theme to your wordpress.</li>
          <li>Use the Upload link in the top sub-menu to upload a zipped copy of Editorial theme that you have previously downloaded from our website.</li>
          </ol>
          <h4>Enabling the Editorial theme</h4>
          <ol>
          <li>Log in to the WordPress Administration Panel.</li>
          <li>Select the Appearance panel, then Themes.</li>
          <li>From the Available Themes section, click on the Editorial (or Theme screenshot)</li>
          <li>A preview of the Editorial theme will be shown.</li>
          <li>Activate Editorial theme by clicking the Activate "Editorial" link in the top right.</li>
          </ol>
          <p>Your selection should immediately become active.</p>
          <p>Note: If the Theme preview is blank, do not activate the new Theme without investigating further. Your site may not be displayed correctly, otherwise.</p>
          </div>
